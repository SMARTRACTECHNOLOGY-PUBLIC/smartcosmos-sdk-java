/**
 * Scoped objects are specialized entities that can derive additional security limitations based on the authorized
 * scopes of an authenticated user through the OAuth2 system. This is different from a typed object, and is left
 * intentionally independent so that access control is not dependent on object types.
 *
 */
public interface IScopedObject
{
    String getScopeUri();

    void setScopeUri(String scopeUri);
}
